void run()
{
	ADD add = { 0 };
	int select = 0;

	while (1)
	{
		select = menu(select);

		switch (select)
		{
		case 1:
			inputAdd(add);
			break;
		case 2:
			inputRetouch(add);
			break;
		case 3:
			statistics(add);
			break;
		case 4:
			checkRecord(add);
			break;
		default:
			return 0;
		}
	}
	
	return;
}

